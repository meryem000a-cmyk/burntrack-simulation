#!/usr/bin/env python3
"""
Flora Dataset: Detection → Classification Converter
=====================================================
Converts the broken YOLO detection dataset (all full-frame bboxes)
into an ImageNet-style classification folder structure that works
with `yolo classify train`.

Input layout (current):
    datasets/yolo_flora/
    ├── images/{train,val}/<species>_<id>.jpg
    └── labels/{train,val}/<species>_<id>.txt  (contains: <class_id> 0.5 0.5 1.0 1.0)

Output layout (new):
    datasets/yolo_flora_cls/
    ├── train/
    │   ├── acacia_not_dry/
    │   │   └── acacia_12345.jpg
    │   └── ...
    └── val/
        └── ...

Usage:
    python convert_to_classification.py              # Convert using symlinks (fast, saves disk)
    python convert_to_classification.py --copy       # Convert using file copies
    python convert_to_classification.py --dry-run    # Preview without changes
    python convert_to_classification.py --min-samples 20  # Drop classes with < 20 samples
"""

